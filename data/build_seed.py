# Lots of small story problems via templates
def _story_addsub(idx, a, b, level, kind="story", grade="1"):
    return p(
        slug=f"as-story-pair-{idx:02d}",
        strand="add_sub_structures", level=level, grade_band=grade, kind=kind,
        title=f"{a} and {b}",
        prompt=f"There are {a} ducks in the pond. {b} more swim over. How many ducks now?",
        answer=str(a + b), answer_type="number",
        hints=[f"Start with {a}.", f"Add {b} more.", "Count on or count all."],
        strategies=["count on", "decompose"],
        tags=["change-add-to", grade],
        minutes=2,
    )

for i, (a, b) in enumerate([(2, 3), (4, 5), (6, 4), (5, 7), (8, 6), (9, 4)], start=1):
    lvl = 2 if (a + b) <= 10 else 3
    ADD_SUB.append(_story_addsub(i, a, b, level=lvl))


