def p(**kw):
    """Compact problem builder with sane defaults."""
    kw.setdefault("hints", [])
    kw.setdefault("strategies", [])
    kw.setdefault("materials", [])
    kw.setdefault("tags", [])
    kw.setdefault("answer", "")
    kw.setdefault("answer_type", "open")
    kw.setdefault("explain_prompt", "How do you know? Can you show it another way?")
    kw.setdefault("parent_extension", "")
    kw.setdefault("minutes", 3)
    kw.setdefault("template", None)
    return kw


