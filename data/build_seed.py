"""Build the canonical seed_problems.json.

Run from project root:
    .venv/bin/python data/build_seed.py

Output: data/seed_problems.json with strands, problems, and templates.

Design notes
------------
The bank is hand-curated for Soviet/Russian-style enrichment: emphasis on
mental flexibility, structure, and reasoning ("how do you know?") over
arithmetic drill. Levels run 1..5 across K–2:

    L1  Pre-K / start of K           counting, subitize 1–6, AB patterns
    L2  End of K / start of Grade 1  ten-frames, sums to 10, AAB patterns
    L3  Grade 1 core                 within 20, place value to 100, missing addend
    L4  End of Grade 1 / Grade 2     within 100, two-step, balance equations
    L5  Grade 2 enrichment           multi-constraint logic, combinatorics, proofs

Each problem carries hints (3 levels of scaffolding), strategies a child might
use (so we can track *how* they solved it, not just whether), materials for
manipulative-based variants, and an `explain_prompt` for metacognition.
"""
